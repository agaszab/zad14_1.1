public class Dane {
    private String kilogramy;
    private String gramy;
    private String dekagramy;
    private String metry;
    private String centymetry;
    private String milimetry;

    public Dane( ) {
    }

    public String getKilogramy() {
        return kilogramy;
    }

    public void setKilogramy(String kilogramy) {
        this.kilogramy = kilogramy;
    }

    public String getGramy() {
        return gramy;
    }

    public void setGramy(String gramy) {
        this.gramy = gramy;
    }

    public String getDekagramy() {
        return dekagramy;
    }

    public void setDekagramy(String dekagramy) {
        this.dekagramy = dekagramy;
    }

    public String getMetry() {
        return metry;
    }

    public void setMetry(String metry) {
        this.metry = metry;
    }

    public String getCentymetry() {
        return centymetry;
    }

    public void setCentymetry(String centymetry) {
        this.centymetry = centymetry;
    }

    public String getMilimetry() {
        return milimetry;
    }

    public void setMilimetry(String milimetry) {
        this.milimetry = milimetry;
    }
}
